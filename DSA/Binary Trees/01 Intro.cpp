/* Terminologies

1. Full BT : Each node has either 0 or 2 children
2. Complete BT : all the levels are completely filled 
   except possibly the lowest one, which is filled from the left
3. Perfect BT :  every internal node has exactly two child nodes and 
   all the leaf nodes are at the same level
4. Balanced BT : height can be at max log2(N)
5. Degenerate BT : every parent node has only one child node

*/